# Python Exercise 006 - Double, Triple, Square Root
# Create an algorithm that reads a number and shows its double, triple and square root.

num = int(input("Type a number: "))
print(f"Number typed was {num}...")
print(f"Double: {num * 2}")
print(f"Triple: {num * 3}")
print(f"Sq root: {num ** (1/2):.2f}")
